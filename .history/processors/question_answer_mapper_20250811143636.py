        prompt += "\n\nDANH S√ÅCH ƒê√ÅP √ÅN/L·ªúI GI·∫¢I:\n"
        
        for a in answers[:10]:  # Gi·ªõi h·∫°n 10 ƒë√°p √°n
            prompt += f"\n--- ƒê√°p √°n {a['number']} ---\n{a['text'][:500]}...\n"
        
